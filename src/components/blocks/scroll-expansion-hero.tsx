'use client';

import {
  useEffect,
  useRef,
  useState,
  ReactNode,
  TouchEvent,
  WheelEvent,
} from 'react';
import Image from 'next/image';
import { motion } from 'framer-motion';

interface ScrollExpandMediaProps {
  mediaType?: 'video' | 'image';
  mediaSrc: string;
  posterSrc?: string;
  bgImageSrc?: string;
  bgVideoSrc?: string;
  title?: string;
  date?: string;
  scrollToExpand?: string;
  textBlend?: boolean;
  titleClassName?: string;
  headingAs?: 'h1' | 'h2';
  /** 'single' renders the full title on one line. 'split' (default) splits at the first space with opposite translateX animations. */
  titleLayout?: 'split' | 'single';
  /** Content rendered as an overlay centred on the expanding media pane. Fades in as the title fades out. */
  paneOverlay?: ReactNode;
  children?: ReactNode;
}

const DEFAULT_TITLE_CLASS =
  'text-4xl md:text-5xl lg:text-6xl font-bold text-copy-white transition-none';

const ScrollExpandMedia = ({
  mediaType = 'video',
  mediaSrc,
  posterSrc,
  bgImageSrc,
  bgVideoSrc,
  title,
  date,
  scrollToExpand,
  textBlend,
  titleClassName,
  headingAs = 'h2',
  titleLayout = 'split',
  paneOverlay,
  children,
}: ScrollExpandMediaProps) => {
  const [scrollProgress, setScrollProgress] = useState<number>(0);
  const [showContent, setShowContent] = useState<boolean>(false);
  const [mediaFullyExpanded, setMediaFullyExpanded] = useState<boolean>(false);
  const [touchStartY, setTouchStartY] = useState<number>(0);
  const [isMobileState, setIsMobileState] = useState<boolean>(false);

  const sectionRef = useRef<HTMLDivElement | null>(null);

  useEffect(() => {
    setScrollProgress(0);
    setShowContent(false);
    setMediaFullyExpanded(false);
  }, [mediaType]);

  useEffect(() => {
    const handleWheel = (e: WheelEvent) => {
      if (mediaFullyExpanded && e.deltaY < 0 && window.scrollY <= 5) {
        setMediaFullyExpanded(false);
        e.preventDefault();
      } else if (!mediaFullyExpanded) {
        e.preventDefault();
        const newProgress = Math.min(
          Math.max(scrollProgress + e.deltaY * 0.0009, 0),
          1
        );
        setScrollProgress(newProgress);
        if (newProgress >= 1) {
          setMediaFullyExpanded(true);
          setShowContent(true);
        } else if (newProgress < 0.75) {
          setShowContent(false);
        }
      }
    };

    const handleTouchStart = (e: TouchEvent) => {
      setTouchStartY(e.touches[0].clientY);
    };

    const handleTouchMove = (e: TouchEvent) => {
      if (!touchStartY) return;
      const touchY = e.touches[0].clientY;
      const deltaY = touchStartY - touchY;
      if (mediaFullyExpanded && deltaY < -20 && window.scrollY <= 5) {
        setMediaFullyExpanded(false);
        e.preventDefault();
      } else if (!mediaFullyExpanded) {
        e.preventDefault();
        const scrollFactor = deltaY < 0 ? 0.008 : 0.005;
        const newProgress = Math.min(
          Math.max(scrollProgress + deltaY * scrollFactor, 0),
          1
        );
        setScrollProgress(newProgress);
        if (newProgress >= 1) {
          setMediaFullyExpanded(true);
          setShowContent(true);
        } else if (newProgress < 0.75) {
          setShowContent(false);
        }
        setTouchStartY(touchY);
      }
    };

    const handleTouchEnd = (): void => setTouchStartY(0);
    const handleScroll = (): void => {
      if (!mediaFullyExpanded) window.scrollTo(0, 0);
    };

    window.addEventListener('wheel', handleWheel as unknown as EventListener, { passive: false });
    window.addEventListener('scroll', handleScroll as EventListener);
    window.addEventListener('touchstart', handleTouchStart as unknown as EventListener, { passive: false });
    window.addEventListener('touchmove', handleTouchMove as unknown as EventListener, { passive: false });
    window.addEventListener('touchend', handleTouchEnd as EventListener);

    return () => {
      window.removeEventListener('wheel', handleWheel as unknown as EventListener);
      window.removeEventListener('scroll', handleScroll as EventListener);
      window.removeEventListener('touchstart', handleTouchStart as unknown as EventListener);
      window.removeEventListener('touchmove', handleTouchMove as unknown as EventListener);
      window.removeEventListener('touchend', handleTouchEnd as EventListener);
    };
  }, [scrollProgress, mediaFullyExpanded, touchStartY]);

  useEffect(() => {
    const checkIfMobile = (): void => setIsMobileState(window.innerWidth < 768);
    checkIfMobile();
    window.addEventListener('resize', checkIfMobile);
    return () => window.removeEventListener('resize', checkIfMobile);
  }, []);

  const mediaWidth = 300 + scrollProgress * (isMobileState ? 650 : 1250);
  const mediaHeight = 400 + scrollProgress * (isMobileState ? 200 : 400);
  const textTranslateX = scrollProgress * (isMobileState ? 180 : 150);

  // Title fades out as pane expands; completely gone by 40% scroll
  const titleOpacity = Math.max(0, 1 - scrollProgress * 2.5);
  // Pane starts invisible; fully visible by 25% scroll
  const paneOpacity = Math.min(scrollProgress * 4, 1);
  // Overlay on pane: appears after title is gone (from 40% to 60%)
  const overlayOpacity = Math.max(0, (scrollProgress - 0.4) * 5);
  // Scroll hint fades out almost immediately
  const hintOpacity = Math.max(0, 1 - scrollProgress * 6);

  const firstWord = title ? title.split(' ')[0] : '';
  const restOfTitle = title ? title.split(' ').slice(1).join(' ') : '';
  const resolvedTitleClass = titleClassName ?? DEFAULT_TITLE_CLASS;

  const paneOverlayNode = paneOverlay ? (
    <div
      className='absolute inset-0 flex flex-col items-center justify-center z-20 px-6 rounded-xl'
      style={{
        opacity: overlayOpacity,
        // Radial gradient: strong purple at centre (where text sits), fades to transparent at edges
        // so the image remains visible around the perimeter while text contrast is maintained.
        // Tight scrim only under the text block — image stays fully bright at edges
        background:
          'radial-gradient(ellipse 55% 45% at center, rgba(75,40,98,0.90) 0%, rgba(75,40,98,0.55) 55%, rgba(75,40,98,0) 100%)',
      }}
    >
      {paneOverlay}
    </div>
  ) : null;

  return (
    <div
      ref={sectionRef}
      className='transition-colors duration-700 ease-in-out overflow-x-hidden'
    >
      <section className='relative flex flex-col items-center justify-start min-h-dvh'>
        <div className='relative w-full flex flex-col items-center min-h-dvh'>

          {/* ── Background ── */}
          <motion.div
            className='absolute inset-0 z-0 h-full'
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 - scrollProgress }}
            transition={{ duration: 0.1 }}
          >
            {bgVideoSrc ? (
              <video
                src={bgVideoSrc}
                autoPlay
                muted
                loop
                playsInline
                className='w-screen h-screen object-cover'
              />
            ) : bgImageSrc ? (
              <Image
                src={bgImageSrc}
                alt='Background'
                width={1920}
                height={1080}
                className='w-screen h-screen'
                style={{ objectFit: 'cover', objectPosition: 'center' }}
                priority
              />
            ) : null}
            <div className='absolute inset-0 bg-stride-purple-primary/70' />
          </motion.div>

          <div className='container mx-auto flex flex-col items-center justify-start relative z-10'>
            <div className='flex flex-col items-center justify-center w-full h-dvh relative'>

              {/* ── Expanding media pane ── */}
              <div
                className='absolute z-0 top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 rounded-2xl'
                style={{
                  width: `${mediaWidth}px`,
                  height: `${mediaHeight}px`,
                  maxWidth: '95vw',
                  maxHeight: '85vh',
                  opacity: paneOpacity,
                  boxShadow: '0px 0px 60px rgba(0, 0, 0, 0.5)',
                }}
              >
                {mediaType === 'video' ? (
                  mediaSrc.includes('youtube.com') ? (
                    <div className='relative w-full h-full pointer-events-none'>
                      <iframe
                        width='100%'
                        height='100%'
                        src={
                          mediaSrc.includes('embed')
                            ? mediaSrc + (mediaSrc.includes('?') ? '&' : '?') +
                              'autoplay=1&mute=1&loop=1&controls=0&showinfo=0&rel=0&disablekb=1&modestbranding=1'
                            : mediaSrc.replace('watch?v=', 'embed/') +
                              '?autoplay=1&mute=1&loop=1&controls=0&showinfo=0&rel=0&disablekb=1&modestbranding=1&playlist=' +
                              mediaSrc.split('v=')[1]
                        }
                        className='w-full h-full rounded-xl border-0'
                        allow='accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture'
                        allowFullScreen
                      />
                      <div className='absolute inset-0 z-10' style={{ pointerEvents: 'none' }} />
                      <motion.div
                        className='absolute inset-0 bg-stride-purple-primary/40 rounded-xl'
                        initial={{ opacity: 0.7 }}
                        animate={{ opacity: 0.5 - scrollProgress * 0.3 }}
                        transition={{ duration: 0.2 }}
                      />
                      {paneOverlayNode}
                    </div>
                  ) : (
                    <div className='relative w-full h-full pointer-events-none'>
                      <video
                        src={mediaSrc}
                        poster={posterSrc}
                        autoPlay
                        muted
                        loop
                        playsInline
                        preload='auto'
                        className='w-full h-full object-cover rounded-xl'
                        controls={false}
                        disablePictureInPicture
                        disableRemotePlayback
                      />
                      <div className='absolute inset-0 z-10' style={{ pointerEvents: 'none' }} />
                      <motion.div
                        className='absolute inset-0 bg-stride-purple-primary/40 rounded-xl'
                        initial={{ opacity: 0.7 }}
                        animate={{ opacity: 0.5 - scrollProgress * 0.3 }}
                        transition={{ duration: 0.2 }}
                      />
                      {paneOverlayNode}
                    </div>
                  )
                ) : (
                  <div className='relative w-full h-full'>
                    <Image
                      src={mediaSrc}
                      alt={title || 'Media content'}
                      width={1280}
                      height={720}
                      className='w-full h-full object-cover rounded-xl'
                    />
                    {/* Very light vignette — keeps edges from blowing out, image stays bright */}
                    <div className='absolute inset-0 bg-stride-purple-primary/10 rounded-xl' />
                    {paneOverlayNode}
                  </div>
                )}

                {date && (
                  <div className='flex flex-col items-center text-center relative z-10 mt-4'>
                    <p className='text-xl text-stride-yellow-accent font-semibold'>{date}</p>
                  </div>
                )}
              </div>

              {/* ── Title — entrance via outer motion.div; scroll fade via inner div ── */}
              <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ duration: 0.8, ease: 'easeOut', delay: 0.25 }}
                className={`flex items-center justify-center text-center w-full relative z-10 ${
                  textBlend ? 'mix-blend-difference' : 'mix-blend-normal'
                }`}
              >
                <div
                  className='flex flex-col items-center gap-2'
                  style={{ opacity: titleOpacity }}
                >
                  {titleLayout === 'single' ? (
                    headingAs === 'h1' ? (
                      <h1 className={resolvedTitleClass}>{title}</h1>
                    ) : (
                      <h2 className={resolvedTitleClass}>{title}</h2>
                    )
                  ) : headingAs === 'h1' ? (
                    <>
                      <motion.h1
                        className={resolvedTitleClass}
                        style={{ transform: `translateX(-${textTranslateX}vw)` }}
                      >
                        {firstWord}
                      </motion.h1>
                      <motion.h1
                        className={resolvedTitleClass}
                        style={{ transform: `translateX(${textTranslateX}vw)` }}
                      >
                        {restOfTitle}
                      </motion.h1>
                    </>
                  ) : (
                    <>
                      <motion.h2
                        className={resolvedTitleClass}
                        style={{ transform: `translateX(-${textTranslateX}vw)` }}
                      >
                        {firstWord}
                      </motion.h2>
                      <motion.h2
                        className={resolvedTitleClass}
                        style={{ transform: `translateX(${textTranslateX}vw)` }}
                      >
                        {restOfTitle}
                      </motion.h2>
                    </>
                  )}
                </div>
              </motion.div>

              {/* ── Scroll hint — fades out immediately on scroll ── */}
              {scrollToExpand && (
                <div className='absolute bottom-10 z-10' style={{ opacity: hintOpacity }}>
                  <motion.div
                    className='flex flex-col items-center gap-2 cursor-default select-none'
                    initial={{ opacity: 0, y: 10 }}
                    animate={{ opacity: 1, y: 0 }}
                    transition={{ delay: 0.9, duration: 0.6, ease: 'easeOut' }}
                    whileHover={{ scale: 1.12, transition: { duration: 0.15 } }}
                  >
                    <span className='text-xs text-stride-yellow-accent font-medium tracking-widest uppercase'>
                      {scrollToExpand.replace(/\s*↓\s*$/, '')}
                    </span>
                    <motion.span
                      className='text-stride-yellow-accent text-base leading-none'
                      animate={{ y: [0, 6, 0] }}
                      transition={{ duration: 1.4, repeat: Infinity, ease: 'easeInOut' }}
                    >
                      ↓
                    </motion.span>
                  </motion.div>
                </div>
              )}
            </div>

            {/* ── Post-expand content ── */}
            <motion.section
              className='flex flex-col w-full px-8 py-10 md:px-16 lg:py-20'
              initial={{ opacity: 0 }}
              animate={{ opacity: showContent ? 1 : 0 }}
              transition={{ duration: 0.7 }}
            >
              {children}
            </motion.section>
          </div>
        </div>
      </section>
    </div>
  );
};

export default ScrollExpandMedia;
